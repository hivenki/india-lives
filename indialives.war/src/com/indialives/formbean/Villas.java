package com.indialives.formbean;

public class Villas {
	
	private Integer plotNo;
	private Integer noOfFloors;	
	private String name;
	private Integer noOfBedRooms;
	private Integer noOfParkings;
	
	public Integer getPlotNo() {
		return plotNo;
	}
	public void setPlotNo(Integer plotNo) {
		this.plotNo = plotNo;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getNoOfBedRooms() {
		return noOfBedRooms;
	}
	public void setNoOfBedRooms(Integer noOfBedRooms) {
		this.noOfBedRooms = noOfBedRooms;
	}
	public Integer getNoOfParkings() {
		return noOfParkings;
	}
	public void setNoOfParkings(Integer noOfParkings) {
		this.noOfParkings = noOfParkings;
	}
	public Integer getNoOfFloors() {
		return noOfFloors;
	}
	public void setNoOfFloors(Integer noOfFloors) {
		this.noOfFloors = noOfFloors;
	}
	
	
}
